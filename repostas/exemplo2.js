// Vamos criar variaveis usando let, const e var, atribuir strings e vamos concatenar e interpolar.

let myName = "Ricardo"
const myMiddleName = "Mateus"
var myLastName = "da Costa"

console.log("Meu nome completo concatenado é " + myName, "" + myMiddleName, "" + myLastName);

console.log(`Meu nome completo interpolado é ${myName} ${myMiddleName} ${myLastName}`);